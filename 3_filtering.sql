--MATCHING CONDITION: TRYING TO SEARCH ANY ROW USING ANY COLOUMS PARTIAL STRING MATCH, LIKE BEGIN/END WITH CERTAIN SUBSTRING/CHARACTER USING WILDCARD. '%' --> ANY NUMBER IF CHARACTER AND '_' EXACTLY ONE CHARACTER. ALSO, REGEXP_LIKE CAN BE USED.
SELECT S.CHANNEL,S.MANUFACTURER,P.MOLECULES FROM 
STG_EG.STG_EG_SALES_NATIONAL_MTH S
JOIN  -- JOINING MULTIPLE TABLES
PSA_EG.PSA_EG_SALES_NATIONAL_MTH_TRANSP P
ON S.CHANNEL = P.CHANNEL AND -- EQUALITY CONDITION
S.CORPORATION = P.CORPORATION AND -- EQUALITY CONDITION
S.ATC4 = P.ATC4 -- EQUALITY CONDITION
WHERE S.CHANNEL LIKE '%RETAIL'; --WILDCARD SEARCH

SELECT NAME,COUNTRY_ID,
RANK() OVER (ORDER BY LENGTH(D.COUNTRY_ID) ASC) AS RNK_X FROM
DF_CHC_MARKET_DEV.TECH_TMP.DIM_COUNTRY D
where country_id LIKE 'N_' OR --WILDCARD SEARCH
country_id LIKE '%K' --WILDCARD SEARCH
and name is not null 
QUALIFY RNK_X >= 1 -- TYPE OF FILTER BUT EXCLESUIVE FOR WINDOW FUNCTION.
ORDER BY 3 DESC
LIMIT 10;

SELECT NAME,COUNTRY_ID,
RANK() OVER (ORDER BY LENGTH(D.COUNTRY_ID) ASC) AS RNK_X FROM
DF_CHC_MARKET_DEV.TECH_TMP.DIM_COUNTRY D
where REGEXP_LIKE(D.COUNTRY_ID, '^[MU].*') -- STARTS WITH M OR U AND CAN HAVE ANY NUMBER OF CHARACTER IN COUNTRY_ID COLOUMN.
and name is not null 
QUALIFY RNK_X >= 1 -- TYPE OF FILTER BUT EXCLESUIVE FOR WINDOW FUNCTION.
ORDER BY 3 DESC
LIMIT 10;

--NULL VALUES: AN EXPRESSION CAN BE NULL, BUT IT CAN NEVER EQUAL NULL. TWO NULLS ARE NEVER EQUAL TO EACH OTHER.
SELECT 'YES' AS IS_VALID WHERE NULL = NULL; --USES FILTER CONDITION NULL = NULL; ALWAYS EVELUATE AS FALSE
SELECT 'YES' AS IS_VALID WHERE NULL IS NULL; --USES 'IS' NULL OPERATOR, WHICH IN THIS CASE EVALUATES AS TRUE.

