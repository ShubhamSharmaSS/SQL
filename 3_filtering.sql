--CONDITION COMPONENTS: A CONDITON IS MADE UP OF ONE ORE MORE EXPRESSION COMBINED WITH ONE OR MORE OPERATORS. IT CAN CONTAIN COLOUMN OF A TABLE,NUMBER,DATE,STRING LITERLAS,BUILTIN FUNCTIONS, SUBQUERY AND LIST OF EXPRESSION. OPERATOR USED WITHIN CONDITION INCLUDES (COMPARISON OPERATOR =,<,>,!=,<>) AND (ARITHMETIS OPERATOR +,-,*,/).
--EQUALITY CONDITION: NAME = 'ABC'/ORDER_DATE = '02/09/2025' --> THERE ARE KNOWN AS EQUALITY CONDITION,CAUSE THEY EQUATE ONE EXPRESSION TO ANOTHER. MOSTLY USE FOR JOINING/FILTERING.
SELECT S.CHANNEL,S.MANUFACTURER,P.MOLECULES FROM 
STG_EG.STG_EG_SALES_NATIONAL_MTH S
JOIN  -- JOINING MULTIPLE TABLES
PSA_EG.PSA_EG_SALES_NATIONAL_MTH_TRANSP P
ON S.CHANNEL = P.CHANNEL AND -- EQUALITY CONDITION
S.CORPORATION = P.CORPORATION AND -- EQUALITY CONDITION
S.ATC4 = P.ATC4 -- EQUALITY CONDITION
WHERE S.CHANNEL LIKE '%RETAIL'; -- FILTER

--INEQUALITY CONDITION: COMMON TYPE OF CONDITION WHICH EVALUATES AS TRUE WHEN TWO EXPRESSION ARE NOT SAME.
SELECT S.CHANNEL,S.MANUFACTURER,P.MOLECULES FROM 
STG_EG.STG_EG_SALES_NATIONAL_MTH S
JOIN  -- JOINING MULTIPLE TABLES
PSA_EG.PSA_EG_SALES_NATIONAL_MTH_TRANSP P
ON S.CHANNEL = P.CHANNEL AND -- EQUALITY CONDITION
S.CORPORATION = P.CORPORATION AND -- EQUALITY CONDITION
S.ATC4 = P.ATC4 -- EQUALITY CONDITION
WHERE (S.CHANNEL <> '%RETAIL' OR UPPER(S.CHANNEL != 'HOSPITAL'); --INEQUALITY CONDITION

--RANGE CONDITION: DETERMINE IF AN EXPRESSION FALLS WITHIN A SPECIFIED RANGE. COMMONLY USED WITH NUMERIC,DATE AND STRINGS. UTILIZE THE BETWEEN OPERATOR.
SELECT * FROM DWH_MARKET_SALES.FCT_SALES_NATIONAL_MTH F WHERE F.COUNTRY_ID BETWEEN 9 AND 12 ; -- 9 AND 12 ARE ALSO INCLUDED IN RESULT
SELECT * FROM DWH_MARKET_SALES.FCT_SALES_NATIONAL_MTH F WHERE F.INSERTED_TS BETWEEN TO_DATE('2025-10-28', 'YYYY-MM-DD') AND TO_DATE('2025-10-30', 'YYYY-MM-DD');
SELECT * FROM DF_CHC_MARKET_DEV.TECH_TMP.DIM_COUNTRY WHERE COUNTRY_ID BETWEEN 'IN' AND 'IX';

--MEMBERSHIP CONDITION: BINDING MULTIPLE CONDION IN A SINGLE STATEMENT IS MEMBERSHIP CONDITION. IT USES 'IN' OPERATOR ALSO 'NOT' CAN BE INCLUDED IF REQUIRED.
SELECT S.CHANNEL,S.MANUFACTURER,P.MOLECULES FROM 
STG_EG.STG_EG_SALES_NATIONAL_MTH S
