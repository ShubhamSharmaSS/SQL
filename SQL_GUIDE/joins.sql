--INNER JOINS: IT IS MOST COMMONLY USED TYPE OF JOIN, ALSO ARE THE TYPE IF DEFAULT JOINS, CAUSE NO NEED TO SPECIFY THE inner KEYWORD IN from CLAUSE.
-- RESULT SET INCLUDES ONLY THOSE ROWS FOR WHICH THERE IS A VALID JOIN BETWEEN TWO TABLES.
--ONLY MATCHING ROWS FROM BOTH THE TABLE.

CREATE TABLE STG_EG.customer_simple_PRACTICE (custkey, custname) AS SELECT * FROM
(VALUES (101,'BOB'),(102,'KIM'),(103,'JIM'));

CREATE OR REPLACE TABLE STG_EG.order_simple_PRACTICE (ordernum, custname) AS SELECT * FROM
(VALUES (990,101),(991,102),(992,101),(993,104));

SELECT O.ORDERNUM, O.CUSTNAME, C.CUSTNAME FROM STG_EG.order_simple_PRACTICE AS O INNER JOIN STG_EG.customer_simple_PRACTICE AS C ON O.CUSTNAME = C.CUSTKEY;

--LEFT OUTER JOINS: COMMON TYPE OF JOIN, RETURNS ALL OF THE ROWS FROM TABLE A REGARDLESS OF WHETHER A JOIN EXISTS TO TABLE B. JOIN WAS SPECIFIED TO BE LEFT OUTER, MEANING THAT ALL ROWS FROM THE TABLE ON THE LEFT SIDE OF THE JOIN (ORDER_SIMPLE TABLE) SHOULD BE INCLUDED. ALL ELEMENTS FROM LEFT TABLE & MATCHING RECORDS FROM RIGHT TABLE.

SELECT O.ORDERNUM, O.CUSTNAME, C.CUSTNAME FROM STG_EG.order_simple_PRACTICE AS O LEFT OUTER JOIN STG_EG.customer_simple_PRACTICE AS C ON O.CUSTNAME = C.CUSTKEY;

--RIGHT OUTTER JOIN:RARELY USED JOIN, RETURNS ALL OF THE ROWS FROM TABLE B REGARDLESS OF WHETHER A JOIN EXISTS TO TABLE A. JOIN WAS SPECIFIED TO BE RIGHT OUTER, MEANING THAT ALL ROWS FROM THE TABLE ON THE RIGHT SIDE OF THE JOIN (CUSTOMER_SIMPLE TABLE) SHOULD BE INCLUDED.

SELECT O.ORDERNUM, O.CUSTNAME, C.CUSTNAME FROM STG_EG.order_simple_PRACTICE AS O RIGHT OUTER JOIN STG_EG.customer_simple_PRACTICE AS C ON O.CUSTNAME = C.CUSTKEY;

--CROSS JOINS: THE CROSS JOIN, WHICH IS A BIT OF A MISNOMER (THAT IS APPROPRIATE/WRONG) BECAUSE NO ACTUAL JOIN IS HAPPENING. RATHER THE TWO TABLES ARE MERGED TOGETHER, WHICH IS KNOWN AS CARTESIAN PRODUCT OF THE TWO TABLE. CROSS JOIN ON A TABLE WITH 50 ROWS TO ANOTHER WITH 150 ROWS, THE RESULT SET WOULD CONTAIN 7500 ROWS (50*150).
--ALSO CAN BE USE AS TO CONSTUCT DATA ON THE FLY.

--GENERATING TABLE USEING CROSS JOIN : GENERATE A QUARTERLY REPORT SPANNING THE YEAR 2020,2021 & 2022, BUT THERE ISN'T ANY TABLE IN DATABASE THAT CONTAINS INFORMATION ABOUT QUARTERS. GENERATE A ARESULT SET TO USE FOR YOUR QUERY TAHT INCLUDES THE 12 NEEDE ROWS (3 YEAR * 4 QUARTERS) BY BUILDING TWO DATA SETS AND MERGING THEM USING CROSS JOIN.
-- THE CROSS JOIN DOES NOT INCLUDE THE ON SUBCLAUSE, SINCE THERE IS NO JOIN BEING SPECIFIED. 

SELECT YEARS.YEARNUM, QTRS.QTRNAME, QTRS.STARTMONTH, QTRS.ENDMONTH
FROM
(VALUES (2020),(2021),(2022)) AS YEARS (YEARNUM) 
CROSS JOIN 
(VALUES ('Q1',1,3),('Q2',4,6),('Q3',7,9),('Q4',10,12))
AS QTRS (QTRNAME,STARTMONTH,ENDMONTH) ORDER BY 1,2;

