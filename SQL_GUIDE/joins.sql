--JOINS: TO BRING TOGETHER DATA OF MULTIPLE TABLES IN ORDER TO RETRIEVE DATA IS KNOWS AS JOINING OF TABLES.
--IT USES UNIQUE IDENTIFIER TO JOIN TABLES.
--THE from CLAUSE LISTS THE TABLES TO BE JOINED AND THE on SUBCLAUSE CONTAINS THE JOIN CONDITION/S NEEDED TO TRANSVERSE FROM ONE TO ANOTHER TABLE.
SELECT S.CHANNEL,S.MANUFACTURER,P.MOLECULES,LC_60,NVL(LC_58,0) FROM --NVL FUNCTION WILL SUBSTITUE THE VALUE 0 FOR ANY NULL VALUES.
STG_EG.STG_EG_SALES_NATIONAL_MTH S
JOIN  -- JOINING 2 TABLES
PSA_EG.PSA_EG_SALES_NATIONAL_MTH_TRANSP P
ON S.CHANNEL = P.CHANNEL AND -- EQUALITY CONDITION
S.CORPORATION = P.CORPORATION AND -- EQUALITY CONDITION
S.ATC4 = P.ATC4 -- EQUALITY CONDITION
WHERE S.CHANNEL LIKE '%RETAIL' --WILDCARD SEARCH
AND S.ATC4 <> 'ATC4'
AND NVL(LC_60,0) --NVL FUNCTION WILL SUBSTITUE THE VALUE 0 FOR ANY NULL VALUES
ORDER BY LC_60
LIMIT 100;

