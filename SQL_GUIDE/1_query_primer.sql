--###CHAPTER-2 FILTERING ####
--CONDITON EVALUATION: WHERE CLAUSE is uesd, we can add one or more condition using 'AND' / 'OR'; it returns result when condition is TRUE, for more precise conditioning use parenthesis '()'.

SELECT NAME,COUNTRY_ID,
RANK() OVER (ORDER BY LENGTH(D.COUNTRY_ID) ASC) AS RNK_X FROM
DF_CHC_MARKET_DEV.TECH_TMP.DIM_COUNTRY D
where (country_id = 'NG' or country_id = 'MK') and name is not null -- 'and' is a must sastifactory condition, and for 'or' any one is fine.
QUALIFY RNK_X >= 1 -- TYPE OF FILTER BUT EXCLESUIVE FOR WINDOW FUNCTION.
ORDER BY 3 DESC
LIMIT 10;

--USING NOT OPERATOR: IN RETRIVING ROWS FOR EVERYONE EXCEPT THOSE FOUND
SELECT NAME,COUNTRY_ID,
RANK() OVER (ORDER BY LENGTH(D.COUNTRY_ID) ASC) AS RNK_X FROM
DF_CHC_MARKET_DEV.TECH_TMP.DIM_COUNTRY D
where NOT (country_id = 'NG' or country_id = 'MK') and name is not null -- 'and' is a must sastifactory condition, and for 'or' any one is fine, BUT FETECHING ALL ROWS EXCEPT 'NG'&'MK'
QUALIFY RNK_X >= 1 -- TYPE OF FILTER BUT EXCLESUIVE FOR WINDOW FUNCTION.
ORDER BY 3 DESC
LIMIT 10;

SELECT NAME,COUNTRY_ID,
