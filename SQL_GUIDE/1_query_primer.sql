--QUERY CLAUSES: There are 8 different clauses that can be add-up in a query.
--select: speciies the coloumn to be included in the result set.
--from: identifies the table from which data should be pulled.
--where: remove unwanted rows in the result (a type of filter).
--group by: group rows together by common values.
--having: remove unwanted rows in the result set based on grouping.
--qualify: remove unwanted rows in the result set base in result of windoing function.
--order by: sorts the result by one or more coloumn.
--limit: restrics numbers of rows in result set.

