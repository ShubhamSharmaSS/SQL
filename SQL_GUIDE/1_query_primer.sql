--THE GROUP BY CLAUSE: USED FOR GROUPPING ROWS, USEFUL FOR REPORTING AND DATA ANALYSIS. 
--EVELUATION:WHERE->GROUP BY -> HAVING

SELECT COUNTRY_ID,COUNT(COUNTRY_ID)  FROM
DF_CHC_MARKET_DEV.DWH_MARKET_SALES.FCT_SALES_NATIONAL_MTH
GROUP BY COUNTRY_ID;

--THE HAVING CLAUSE: CONCEPT OF FILTERING
SELECT COUNTRY_ID,COUNT(COUNTRY_ID)  FROM
DF_CHC_MARKET_DEV.DWH_MARKET_SALES.FCT_SALES_NATIONAL_MTH
WHERE CALENDAR_ID LIKE '2025%'  -- 1 RESOLVE
GROUP BY COUNTRY_ID  --2 RESOLVING
HAVING (COUNT(COUNTRY_ID) >= 5000); --3 RESOLVING

--THE QUALIFY CLAUSE: THIS IS 3RD CLAUSE FOR FILTERING AFTER WHERE & HAVING, BUT IT IS A SPECIAL-PURPOSE CLAUSE USED ONLY FOR FILTERING ROW BASED ON RESULT OF WINDOWING FUNCTIONS (RANK()).
-- FOR NORMAL ROW FILTER USE WHERE CLAUSE, FOR FILTERING ON BASIS OF GROUP BY CLAUSE USE HAVING SUBCLAUSE AND FOR FILTERING USING ANY WINDOW FUNCTION USE QUALIFY SUBCLAUSE.

SELECT NAME,
RANK() OVER (ORDER BY LENGTH(D.NAME) ASC) AS RNK_X FROM
DF_CHC_MARKET_DEV.TECH_TMP.DIM_COUNTRY D
QUALIFY RNK_X >= 10;

