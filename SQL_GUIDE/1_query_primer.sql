--THE ORDER BY CLAUSE: USE TO SORT THE RESULT ON BASIS OF ALPHABETICALLY, NUMERICALLY OR CHRONOLOGICALLY, USE AT END OF QUERY BY PROVIDING COLOUMN NAME OR COLOUMN POSITION.

