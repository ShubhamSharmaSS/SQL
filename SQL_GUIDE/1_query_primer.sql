where (country_id <> 'NG' OR country_id <> 'MK') and name is not null  -- 'and' is a must sastifactory condition, and for 'or' any one is fine. BUT FETECHING ALL ROWS EXCEPT 'NG'&'MK'. NOTE: '<>' IS SHORT HAND FOR 'NOT'.
QUALIFY RNK_X >= 1 -- TYPE OF FILTER BUT EXCLESUIVE FOR WINDOW FUNCTION.
