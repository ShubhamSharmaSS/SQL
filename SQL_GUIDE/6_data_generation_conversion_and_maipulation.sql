---NUMERIC CONVERSION:TO CONVERT A STRING TO A NUMBER, THERE ARE SEVERAL STRATEGIES: EXPLICIT CONVERSION, WHERE YOU SPECIFY THAT THE STRING BE CONVERTED TO A NUMBER, AND IMPLICIT CONVERSION (ALSO KNOWN AS COERCION), WHERE SNOWFLAKE SEES THE NEED FOR THE CONVERSION AND ATTEMPTHS TO DO SO.

SELECT 123.45 AS REAL_NUM
UNION
SELECT '678.90' AS REAL_NUM; -- IMPLICIT CONVERSION (ALSO KNOWN AS COERCION)

SELECT 123.45 AS REAL_NUM 
UNION
SELECT 'AAA.BB' AS REAL_NUM; --Numeric value 'AAA.BB' is not recognized.

--EXPLICITLY REQUEST A DATA CONVERSION USING ONE OF THE FOLLOWING LIKE cast() FUNCTION, cast operator ::, SPECIFIC CONVERSION FUNCTION SUCH AS to_decimal().

SELECT CAST(STR.VAL AS NUMBER(7,2)) AS CAST_VAL,
STR.VAL::NUMBER(7,2) AS CAST_OPERATOR_VAL,
TO_DECIMAL(STR.VAL,7,2) AS TO_DEC_VAL,
TO_DECIMAL(STR.VAL1, '$999.99',7,2) AS CHARACTER_IMBEDMENT, -- ADDING A NONNUMERIC CHARACTER IN FRONT OF VALUE
TRY_TO_DECIMAL(STR.VAL1,'$9999.99',7,2) AS GOOD, -- TRY_TO_DECIMAL WILL TEST CONVERSION AND RETURNS NULL IF CONVERSION FAIL INSTEAD OF ERROR.
TRY_TO_DECIMAL(STR.VAL1,'999.99',7,2) AS BAD -- HERE WE ARE KEEPING LESS DIGITS AND NOT PROVIDING $ SYMBOL.
FROM VALUES ('157.78','$157.78') AS STR(VAL,VAL1); --(IF HERE, THERE IS ANY NON-NUMERIC VALUE IN STR.VAL THE CONVERSION WILL FAIL, TO USE OR DEFINE ANY NON-NUMERIC CHARACER USE STR.VAL1 STYLE).

