--STRING GENERATION AND MANIPULATION
SELECT 'HERE IS MY STRING'; -- A STRING IS A SIMPLE MATTER OF ENCLOSING UR STRING IN SINGLE QUOTES.
SELECT 'STRING 1' || 'AND' || 'STRING 2'; -- CONCATENATE STRING USING ||.
SELECT CONCAT('STRING 1','AND','STRING 2'); -- CONCATENATING USING BUILTIN FUNCTION concat().

-- IF ANT SYMBOL IS NOT PRESENT ON KEYBOARD THEN USE ASCII VALUE IN char() FUNCTION 
SELECT CONCAT('I SPENT ',CHAR(8364),' 123 IN PARIS. '); --O/P:I SPENT € 123 IN PARIS. 

--CHANGING CASE OF A STRING.
SELECT UPPER(STR.VAL), --I VISIT PARIS; ALL IN CAPITAL LETTERS
LOWER(STR.VAL), --i visit paris; ALL IN SMALL LETTERS
INITCAP(STR.VAL) -- I Visit Paris ; ALL IN TITLE CASE.
FROM VALUES('I VISIT PARIS') AS STR(VAL);

--REVERSING THE STRING
SELECT REVERSE('?SIHT DAER UOY NAC'); -- O/P: REVERSING THE STRING: CAN YOU READ THIS?

--USING TRIM FOR REMOVING THE EMPTY SPACES
SELECT LTRIM(STR.VAL), --REMOVE LEFT SPACE
RTRIM(STR.VAL), --REMOVE RIGHT SPACE
TRIM(STR.VAL) --REMOVE BOTH SIDE SPACE
FROM VALUES('  ABC  ') AS STR(VAL);

--LENGHT FUNCTION: RETURN LENGHT OF A STRING.
SELECT LENGTH(LTRIM(STR.VAL)), --REMOVE LEFT SPACE LENGTH IS 5
LENGTH(RTRIM(STR.VAL)), --REMOVE RIGHT SPACE LENGTH IS 5
LENGTH(TRIM(STR.VAL)) --REMOVE BOTH SIDE SPACE LENGTH IS 3
FROM VALUES('  ABC  ') AS STR(VAL);

--REPLACE OF A CHARACTER. USE translate TO CONVERT OR replace ANY CHARATER IN STRING.
SELECT TRANSLATE('(+91) 123456789', '()-',''); -- FROM (+91) 123456789 REPLACE '()-' <-- THESE CHARACTERS TO '', O/P: +91 123456789
SELECT TRANSLATE('AxByCz','ABC','XYZ'); --REPLACE A SET OF CHARACTER WITH DIFFERENT SET. : XxYyZz

--STRING SEARCHING AND EXTRACTING: 
--position() FUNCTION: FIND POSITION OF A STRING WITHIN A STRING, ELSE 0 IF THE SUBSTRING IS'NT FOUND. ALSO ALLOWS TO SPECIFY THE STARTING POSITION.

SELECT POSITION('HERE',STR.VAL) AS POS1, --O/P: 1
POSITION('HERE',STR.VAL,10) AS POS2, -- SEARCHING BEGINS FROM 10TH INDEX O/P: 23
POSITION('NOWHERE',STR.VAL) AS POS3 -- O/P: 0
FROM VALUES('HERE THERE, AND EVERYWHERE') AS STR(VAL);

--SUBSTR(): SHOWS PART OF STRING FROM SPECIFIED POSITION AND STARTING POSITION AS AN OPTIONAL NUMBER OF CHARACTER
SELECT SUBSTR(STR.VAL, 2,10) AS STARTING_OF_STRING,  --BEGINNING (FETCHING STARTS FROM 2 AND ENDS ON 10)
SUBSTR(STR.VAL,12) AS REST_OF_STRING  --ENDING' (FETCHING ENDS ON 12)
FROM VALUES($$'BEGINNING ENDING'$$) AS STR(VAL);

--startswith(): A BOOLEN TYPE FUNCTION,WHICH RETURN STRING IF STRING STARTS WITH SPECIFIED CHARACTER ELSE NO OUTPUT
SELECT STR.VAL 
FROM VALUES ('HERE, THERE AND EVERYWHERE') AS STR(VAL)
WHERE STARTSWITH(STR.VAL,'WHERE');  -- NO RESULT

SELECT STR.VAL 
FROM VALUES ('HERE, THERE AND EVERYWHERE') AS STR(VAL)
WHERE STARTSWITH(STR.VAL,'HERE');  --HERE, THERE AND EVERYWHERE

--endswith(): A BOOLEN TYPE FUNCTION,WHICH RETURN STRING IF STRING ENDS WITH SPECIFIED CHARACTER ELSE NO OUTPUT
SELECT STR.VAL 
FROM VALUES ('HERE, THERE AND EVERYWHERE') AS STR(VAL)
WHERE ENDSWITH(STR.VAL,'HERE'); --'HERE, THERE AND EVERYWHERE'

--contains():  A BOOLEN TYPE FUNCTION,WHICH RETURN STRING IF STRING IS PRESENT WITH SPECIFIED CHARACTER ELSE NO OUTPUT
SELECT STR.VAL 
FROM VALUES ('HERE, THERE AND EVERYWHERE') AS STR(VAL)
WHERE CONTAINS(STR.VAL,'AND'); --'HERE, THERE AND EVERYWHERE'

