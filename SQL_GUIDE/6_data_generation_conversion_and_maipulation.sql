-- FUNCTIONS RETURNING INTEGER VALUE FOR DATES
--date_part() USE TO EXTRACT VARIOUS UNIT OF DATE AND TIME IN INTEGER FORMAT.
SELECT DATE_PART(YEAR,DT.VAL) AS YEAR, --2025
DATE_PART(QUARTER,DT.VAL) AS QUARTER, --3
DATE_PART(MONTH,DT.VAL) AS MONTH, --7
DATE_PART(WEEK,DT.VAL) AS WEEK, --30
DATE_PART(HOUR,DT.VAL1) AS HOUR, --7
DATE_PART(MINUTE,DT.VAL1) AS MINUTE, --24
DATE_PART(SECOND,DT.VAL1) AS SECOND, --23
DATE_PART(NANOSECOND,DT.VAL1) AS NANOSECOND --209000000
FROM VALUES(TO_DATE('2025-07-23','yyyy-mm-dd'),CURRENT_TIMESTAMP()) AS DT(VAL,VAL1);

--datediff(): FUNCTION RETURN DIFFERENCE BETWEEN 2 DATES. REGARDLESS OF BIGGERDATE - SMALLERDATE OR VISE-VERSA, YOU CAN WRAP CELL IN abs() TO GET DIRECT VALUE RATHER THAN -VE SIGN.

SELECT DATEDIFF(YEAR,DT.VAL1 ,DT.VAL2) NUM_YEAR, --27
DATEDIFF(MONTH,DT.VAL1 ,DT.VAL2) NUM_MONTH, --324
DATEDIFF(DAY,DT.VAL1 ,DT.VAL2) NUM_DAY, --9878
DATEDIFF(HOUR,DT.VAL1 ,DT.VAL2) HOUR, --237072
FROM VALUES(TO_DATE('12-FEB-1999','DD-MON-YYYY'), CURRENT_DATE()) AS DT(VAL1,VAL2);

--DATE CONVERSION: 
--USE to_date() FUNCTION TO CONVERT STRING TO DATE, THIS CAN ALSO BE DONE BY USING cast().
SELECT CAST('23-SEP-2023' AS DATE) AS FORMAT1, --2023-09-23
CAST('09/23/2024' AS DATE) AS FORMAT2,  --2023-09-23
CAST('2023-09-23' AS DATE) AS FORMAT3;  --2023-09-23
--ABOVE FORMAT DATE CAN BE EASILY RESOLVED BY SNOWFLAKE.SOMETIME IT CAN MAKE MISTAKE, SO USE TRY_CAST() TO AVOID ERROR, AND GET RESULT OR NULL.

SELECT CAST('09-23-2024' AS DATE); --Date '09-23-2024' is not recognized
SELECT TRY_CAST('09-23-2024' AS DATE); --NULL

