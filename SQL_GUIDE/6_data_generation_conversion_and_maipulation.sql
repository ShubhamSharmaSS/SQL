--MANIPULATIG DATES AND TIMESTAMPS
--TEMPORAL FUNCTIONS THAT RETURN DATES: 
--date_trunc(): RETRIEVE A DATE FROM COLOUMN AND DETERMNINE BEGNING OF MONTH, QUARTER,YEAR ASSOCIATED WITH THAT DATE.

SELECT DATE_TRUNC('YEAR',DT.VAL) AS START_OF_YEAR, --2026-01-01
