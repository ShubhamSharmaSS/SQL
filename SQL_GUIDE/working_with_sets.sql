--THE INTERSECT OPERATOR: IT IS USED TO FIND THE INTERSECTION BETWEEN TWO DATA SETS.
SELECT INTEGER_VAL FROM (VALUES (1),(2),(4),(7),(9)) AS SET_A (INTEGER_VAL)
INTERSECT
SELECT INTEGER_VAL FROM (VALUES (3),(5),(7),(9)) AS SET_B (INTEGER_VAL); -- ONLY TAKING COMMON VALUE FROM BOTH SETS.

--THE EXCEPT OPERATOR: SET OPERATOR IS USED TO RETURN ROWS FROM SET A WITHOUT ANY OVERLAP WITH SET B
SELECT INTEGER_VAL FROM (VALUES (1),(2),(4),(7),(9)) AS SET_A (INTEGER_VAL)
EXCEPT
SELECT INTEGER_VAL FROM (VALUES (3),(5),(7),(9)) AS SET_B (INTEGER_VAL); -- TAKING ALL VALUES FROM SET A AND REMOVING SET B VALUES FROM SET A

SELECT INTEGER_VAL FROM (VALUES (3),(5),(7),(9)) AS SET_B (INTEGER_VAL)
EXCEPT
SELECT INTEGER_VAL FROM (VALUES (1),(2),(4),(7),(9)) AS SET_A (INTEGER_VAL); -- TAKING ALL VALUES FROM SET B AND REMOVING SET A VALUES FROM SET B

