--INTRACT WITH YOUR DATA ONE ROW AT A TIME, RELATIONAL DATABASE ARE RELLY ALL ABOUT SETS,THE USE OF sets operators, WHICH ALLOWS YOU TO COMBINE DATA FROM MULTIPLE RESULT SETS.
--SET OPRATORS ARE UNION,INTERSECT AND EXCEPT <-- USING THIS WE CAN COMBINE MULTIPLE DATA.
-- THESE POWERFUL TOOLS ARE USED TO PERFORM DATA ANALYSIS AND CRAFTING REPORTS.

--SET THEORY PRIMER
-- A = {1,2,4,7,9}; B = {3,5,7,9} --> A UNION B {1,2,3,4,5,7,9} -- ALL ELEMENT ARE SORTED AND UNIQUE ELEMENTS FROM BOTH THE SETS.
-- A = {1,2,4,7,9}; B = {3,5,7,9} --> B UNION A {1,2,3,4,5,7,9}
-- A = {1,2,4,7,9}; B = {3,5,7,9} --> A INTERSET B {7,9}
--A = {1,2,4,7,9}; B = {3,5,7,9} --> B INTERSET A {7,9}
-- A = {1,2,4,7,9}; B = {3,5,7,9} --> A EXCEPT B {1,2,4} (EXCEPT MAY ALSO USE AS minus UNTERCHANGEABLY)
-- A = {1,2,4,7,9}; B = {3,5,7,9} --> B EXCEPT A {3,5} -- OUT OF THREE SET OPERATION except IS ONLY ONE THAT YIELDS A DIFFERENT RESULT IF YOU SWITCH THE ORDER OF THE SET.
-- MYSTERY OPERATION: ADVANCE USING OF BASIC SET OPRATION : (A UNION B) EXCEPT (A INTERSECTION B) OR (A EXCEPT B) UNION (B EXCEPT A)

--THE UNION OPERATOR: ALLOWS YOU TO COMBINE TWO OR MORE DATA SETS..
SELECT 1 AS NUMERIC_COL , 'ABC' AS STING_COL 
UNION
SELECT 5 AS NUMERIC_COL , 'XYZ' AS STING_COL; -- HERE, IT IS ABOUT SIMPLE AS IT GETS: TWO SINGLE-ROW RESULT SETS, WITHOUT ANY OVERLAPPING VALUES, COMBINING TO MAKE A TWO-ROW RESULT SET. THIS TYPE OF STATEMENT IS KNOWN AS A compound query BECAUSE IT CONTAIN TWO INDEPENDENT QUERIES SEPERATED BY A SET OPERATOR.
-- RESTRICTIONS RELATED TO THE TWO SET BEING COMBINED: BOTH SETS MUST OF SAME COLOUMN AND DATA TYPE SHOULD BE MATCHING RESPECTIVELY.
SELECT 1 AS NUMERIC_COL , 'ABC' AS STING_COL 
UNION
SELECT 5 AS NUMERIC_COL , 'XYZ' AS STING_COL,99 AS EXTRA_COL; -- SQL compilation error: invalid number of result columns for set operator input branches, expected 2, got 3 in branch 2
