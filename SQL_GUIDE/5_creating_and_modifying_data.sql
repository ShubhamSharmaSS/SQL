--DATA TYPE:
--MAJOR DATA TYPES ARE CHARACTER,NUMERIC,TEMPORAL
--VARCHAR -> MAX SIZE 16MB -> STORES CHARACTER DATA USING UNICODE UTF-8 CHARACTER SET. EG: movie_name varchar(100) --> MAX LENGTH IS 100 BYTES... IF NOT SPECIFIED MAX LENGTH WILL BE ASSIGN I.E. 16MB. SIMILAR TO VARCHAR ARE CHAR,CHARACTER, CHAR VARYING, NVARCHAR, NVARCHAR2,NCHAR,NCHAR VARYING,STRING,TEXT THESE ARE USED BECAUSE PORTING FROM DIFFERENT DB IS EASY. MAJORITY COLOUMN WILL BE DEFINE AS VARCHAR.

--CHAR VERSUS VARCHAR: CHAR IS USED TO STORE FIXED LENGTH OF STRING WITH PAD. EX:CHAR(5) IT WILL HOLD 5 CHARACTER WITH RIGHT PADDED LIKE "CAT"--> "CAT  ". IN SNOWFLAKE CHAR IS OF VARIABLE LENGTH, IT WILL NEVER ADD SPACES TO THE END OF STRING. IN SNOWFLAKE CHAR LENGHT IS 1 BY DEFALULT.

SELECT 'HELLO WORLD' AS OUTPUT_STRING; --> CALLING SRING AS A COLUMN
SELECT 'SNOWFLAKE YOU HAVE''T REACH THE END YET' AS OUTPUT_STRING; --USE 2 APOSTROPHE FOR SINGLE QUOTE.
-- FOR COMPLEX STRING USE 2 DOLLAR SIGN ($$) AS DELIMITER, SNOWFLAKE WILL ACCEPT IT AS IT IS.
SELECT $$ STRING WITH 4 QUOTES ' ' ' ' $$ AS OPSTR;

--NUMERIC DATA: DATA TYPE number IN SNOWFLAKE CAN HANDLE ANY TYPE OF NUMERIC DATA, HOLDS UPTO 38 DIGITS EITHER WITH A DECIMAL POINT OR WITHOUT INTEGER.
--ANY OF THE FOLLOWING VALUE CAN BE ASSIGNED 0,1,-1,0.0000000000000..37 TIMES..0001 OR 1234567.123456789012345678901234567890 (TOTAL 38 DIGITS OR PRECISION). MAXIMUM NUMBER AFTER DECIMAL IS KNOWN AS scale. DEFAULT PRECISION AND SCALE IS number(38,0).
-- IF U WANT A RANGE OF -999.99 TO 999.99 THEN COLOUMN SHOULD BE number(3,2).
-- SNOWFLAKE ACCEPTS DECIMAL,NUMERIC,REAL,TINYINT,SMALLINT,INT,INTEGER,BIGINT,DOUBLE,FLOAT,FLOAT4,FLOAT8 FOR EASY PORTING OF DATA.
--STICK WITH integer FOR INTEGER & number FOR FLOATING NUMBERS

