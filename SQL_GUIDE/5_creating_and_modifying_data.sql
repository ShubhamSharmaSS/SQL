--MODIFYING DATA: USE update TO MODIFY THE EXISTING DATA. USE where CLAUSE TO IDENTIFY THE ROWS THEN USE set CLAUSE TO DESCRIBE THE CHANGES
UPDATE STG_EG.STG_EG_SALES_NATIONAL_MTH 
SET CHANNEL = 'HOSPITAL'  -- SINGLE CHANGE
WHERE (CHANNEL = 'NON RETAIL'  OR CHANNEL = 'NONRETAIL') AND (CHANNEL IS NOT NULL);

UPDATE STG_EG.STG_EG_SALES_NATIONAL_MTH 
SET CHANNEL = 'HOSPITAL', MANUFACTURER = NULL --MULTIPLE CHANGE AT ONCE.
WHERE (CHANNEL = 'NON RETAIL'  OR CHANNEL = 'NONRETAIL') AND (CHANNEL IS NOT NULL);

UPDATE STG_EG.STG_EG_SALES_NATIONAL_MTH AS E
SET LC_1 = SUM(E.LC_1) - 1000 -- SETTING VALUE BASE ON DIFFERENT TABLE.
FROM DF_CHC_MARKET_PROD.DWH_MARKET_SALES.DIM_COUNTRY AS D
WHERE UPPER(D.COUNTRY) = 'EGYPT';

-- BLOCKING UPDATE ON MULTIPLE JOIN; I.E. UPDATEING STATEMENT ON BASIS OF DIFFERENT TABLE WILL BE BLOCKED.
ALTER SESSION SET ERROR_ON_NONDETERMINISTIC_UPDATE = TRUE;

--MERGING DATA: EXECUTING A SINGLE MERGE STATEMENT TO ADD ANY NEW ROW, REMOVE ROWS NO LONGER NEEDED AND MODIFYING ANY EXISTING ROWS. IT WILL CHANGE THE ROW WITHOUT EFFECTIING OTHER ROWS.
MERGE INTO STG_EG.STG_EG_SALES_NATIONAL_MTH AS E
USING DWH_MARKET_SALES.DIM_COUNTRY AS DC 
ON UPPER(DC.COUNTRY) = E.COUNTRY AND E.MANUFACTURER = NULL
WHEN MATCHED AND E.CORPORARION IS NULL THEN DELETE
WHEN MATCHED THEN UPDATE SET E.MANUFACTURER = '0', E.LC_1 = '0', E.LC_60 = '0'
WHEN NOT MATCHED THEN INSERT (CORPORATION,MANUFACTURER) VALUES (DC.CORPORATION, DC.MANUFACTURER);