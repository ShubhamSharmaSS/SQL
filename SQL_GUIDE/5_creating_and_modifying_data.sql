--OTHER DATA TYPES: SPECIAL DATA TYPE, MAINLY USE FOR SEMISTRUCTUTRED DATA SUCH AS JSON DOCUMENT.

--BOOLEAN: KNOWN AS LOGICAL DATA TYPE. HOLD VALUE TRUE OR FALSE. IF REQUIRE BOOLEAN COLOUMN CAN BE NULL.
--FOR TRUE USE: 'TRUE','1','YES','T','Y','ON' (IF USING STRING VALUE).
--FOR FALSE USE: 'FALSE','0','NO,'F','N', 'OFF'  (IF USING INTEGER VALUE).
--0 IS FALSE AND ANY NONZERO IS TRUE (IF USING INTEGER VALUE).

--VARIANT: IT IS A SWISS ARMY KNIFE, IT CAN HOLD ANY TYPE OF DATA. PROVIDE A FLEXIBILITY WHEN STORING SEMISTRUCTURED DATA. INSERTING VALUE INTO A variant COLOUMN USE :: OPERATOR TO CAST A STRING,NUMBER,DATE,ETC.. WE CAN ALSO USE VARIEN TO STORE JSON DOCUMENTS.
SELECT 1::VARIANT,'ABC'::VARIANT,CURRENT_DATE::VARIANT;
-- USE BUILTIN FUNCTION typeof() TO GET DATATYPE BEING USED.
SELECT TYPEOF($$'HELLO WORLD IS A STRING/VARCHAR VALUE'$$::VARIANT); --VARCHAR
SELECT TYPEOF(FALSE::VARIANT); --BOOLEAN
SELECT TYPEOF (CURRENT_TIMESTAMP::VARIANT); --TIMESTAMP_LTZ

--ARRAY: DATA TYPE IS A VARIABLE LENGTH ARRAY OF variant VALUES. CREATE WITH INTIAL LENGTH OF 0 AND EXTEND LATER TO NO UPPER LIMIT ON ELEMENTS. OVERALL SIZE IS 16MB. SQUARE BRACKETS ([]) ARE USED AS DELIMITERS FOR ARRAY LITERALS.
SELECT [123,'ABC',CURRENT_DATE] AS MY_ARRAY; --[
--   123,
--   "ABC",
--   "2026-02-18"
-- ]
--ARRAY CAN BE FLATTENED INTO X ROWS (X=3 IN THIS CASE) BY USING A COMBINATION OF THE table() AND flatten() FUNCTION. BREAKING ALL ELEMENTS TO DIFFERENT ROWS SO THAT WE CAN USE SINGLE ELEMENTS WHEN NEED TO USE FROM THE LIST/ARRAY.
SELECT VALUE FROM TABLE(FLATTEN(INPUT=>  [123,'ABC',CURRENT_DATE] )); --123 /N "ABC" /N "2026-02-18" (/N : NEW LINE)

--OBJECT: STORES AN ARRY IN KEY-VALUE PAIRS. KEYS ARE VARCHAR AND VALUE IS VARIANT. FORMED USING {} AND SEPERATED USING COLONS.
SELECT {'NEW YEAR':'01/01','INDEPENDENCE DAY':'15/08','CHRISTMAS':'25/12'} AS MY_OBJECT;
--USE FLATTEN() AND TABLE() FUNCTION TO TRANSFORM RESULT INTO TABLE WITH TWO COLOUMS OF KEY AND VALUE.
SELECT KEY,VALUE FROM TABLE(FLATTEN({'NEW YEAR':'01/01','INDEPENDENCE DAY':'15/08','CHRISTMAS':'25/12'}));
--ALSO WE CAN USE DIFFERENT CONDITIONS TO RETRIVE DIFFERENT DATAS..
SELECT KEY,VALUE FROM TABLE(FLATTEN({'NEW YEAR':'01/01','INDEPENDENCE DAY':'15/08','CHRISTMAS':'25/12'})) WHERE KEY LIKE '%NEW%';

